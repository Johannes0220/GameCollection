    public void RenderGameState(string currentWord, HangmanGameState state, int incorrectGuesses)
    {
        Console.Clear();
        Console.WriteLine($"Hangman Game - Incorrect Guesses: {incorrectGuesses}");
        Console.WriteLine();
        Console.WriteLine($"Current Word: {currentWord}");
        Console.WriteLine();

        if (incorrectGuesses < Renders.Length)
        {
            Console.WriteLine(Renders[incorrectGuesses]);
        }
        else
        {
            Console.WriteLine(Renders[Renders.Length - 1]);
        }

        if (state == HangmanGameState.Won)
        {
            Console.WriteLine("Congratulations! You won!");
            Console.Clear();
        }
        else if (state == HangmanGameState.Lost)
        {
            /*foreach (var frame in DeathAnimation)
            {
                Console.WriteLine(frame);
            }*/
            Console.WriteLine("Sorry, you lost.");
        }
    }
